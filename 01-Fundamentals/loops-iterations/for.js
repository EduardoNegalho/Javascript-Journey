// for - percorre um bloco de código várias vezes

// Exemplos de usos do for
for (let i = 1; i <= 10; i++) {
    console.log(`${i}° - Hello World!`);
}

for (let i = 0; i <= 10; i++) {
    if (i % 2 === 0) {
        console.log(`${i} é par`);
    } else {
        console.log(`${i} é ímpar`);
    }
}

