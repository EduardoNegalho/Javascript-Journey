/*
Exercício 3: Escreva um programa que receba dois números e retorne a soma, subtração, multiplicação, e divisão deles.
*/

function calcTwoNumbers(a, b) {
    console.log(`${a} + ${b} = ${a + b}`);
    console.log(`${a} - ${b} = ${a - b}`);
    console.log(`${a} * ${b} = ${a * b}`);
    console.log(`${a} / ${b} = ${a / b}`);
}

calcTwoNumbers(17, 8);