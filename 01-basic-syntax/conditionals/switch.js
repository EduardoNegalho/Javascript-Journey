/*
-> O switch case usa uma variável como base, e compara essa variável com diferentes valores.

-> O switch case é uma alternativa ao if/else se você precisar comparar a mesma variável com diferentes valores.
*/

// Exemplo 

const dia = 1;

switch (dia) {
    case 1:
        console.log('Domingo');
        break;
    case 2:
        console.log('Domingo');
        break;
    case 3:
        console.log('Domingo');
        break;
    case 4:
        console.log('Domingo');
        break;
    case 5:
        console.log('Domingo');
        break;
    case 6:
        console.log('Domingo');
        break;
    case 7:
        console.log('Domingo');
        break;
    default:
        console.log('Dia inválido!');
        break;
}